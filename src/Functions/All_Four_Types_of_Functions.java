package Functions;

public class All_Four_Types_of_Functions {

    public void m1(){

        System.out.println("No parameter No return type ");
    }

    public String m2(){

        return "No parameter with the return type";
    }

    public void m3(String name, int age)
    {
        System.out.println(name+" "+age);
    }

    public int m4(int a, int b)
    {
        return a+b;
    }




}
