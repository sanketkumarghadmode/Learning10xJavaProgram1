package Casting;

public class PrimitiveCASTing {

    public static void main(String[] args) {

        int a = 200;
        long b = a;
        System.out.println(b); // implicit casting

        long big  = 7447818516l;

        int a1 = (int) big;
        System.out.println(a1); // explicit casting



    }
}
