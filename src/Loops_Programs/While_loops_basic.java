package Loops_Programs;

public class While_loops_basic {


    public static void main(String[] args) {

        int a = 0;
        while(a<11)
        {
            System.out.println(a++);
        }

       a = 0;
       while (a<5)
       {
           a++;
           System.out.println("Java");
       }




    }

}
