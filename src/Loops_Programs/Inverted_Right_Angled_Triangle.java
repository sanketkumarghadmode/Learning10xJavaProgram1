package Loops_Programs;

public class Inverted_Right_Angled_Triangle {

    public static void main(String[] args) {

        for (int k = 6; k>=0; k--)
        {
            for (int l = 0; l<=k; l++){

                System.out.print("*");
            }
            System.out.println();
        }
    }
}
