package Loops_Programs;

public class Basic_for_loops_programs {

    public static void main(String[] args) {


        for(int i =1; i<= 5 ; i++)
        {
            System.out.println("hello");
        }

        for(int i = 1 ; i<= 10 ; i++)
        {
            System.out.println(i);
        }

        //even number

        for(int j = 0; j<=20; j++)
        {
            if(j%2 ==0)
            {
                System.out.println("Even number "+ j);
            }
        }
    }

}
